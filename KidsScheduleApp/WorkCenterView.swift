// MARK: - ÁªüËÆ°Âç°Áâá
struct StatCard: View {
    let title: String
    let value: String
    let color: Color
    
    var body: some View {
        VStack(spacing: 4) {
            Text(value)
                .font(.title2)
                .fontWeight(.bold)
                .foregroundColor(color)
            Text(title)
                .font(.caption)
                .foregroundColor(.secondary)
        }
        .frame(maxWidth: .infinity)
        .padding(.vertical, 8)
        .background(color.opacity(0.1))
        .cornerRadius(8)
    }
}

// MARK: - Â∑•‰Ωú‰ªªÂä°Âç°Áâá
struct WorkTaskCard: View {
    let task: TaskItem
    var isCompact: Bool = false
    let onProgressUpdate: () -> Void
    
    var body: some View {
        VStack(alignment: .leading, spacing: 8) {
            HStack {
                // Â∑•‰ΩúÊ†áËØÜ
                Image(systemName: "briefcase.fill")
                    .foregroundColor(.blue)
                    .font(.caption)
                
                Text(task.title ?? "Êú™Áü•‰ªªÂä°")
                    .font(isCompact ? .subheadline : .body)
                    .fontWeight(.medium)
                    .lineLimit(1)
                
                Spacer()
                
                if task.isCompleted {
                    Image(systemName: "checkmark.circle.fill")
                        .foregroundColor(.green)
                } else {
                    Button("Êõ¥Êñ∞") {
                        onProgressUpdate()
                    }
                    .font(.caption)
                    .padding(.horizontal, 8)
                    .padding(.vertical, 4)
                    .background(Color.blue.opacity(0.1))
                    .foregroundColor(.blue)
                    .cornerRadius(6)
                }
            }
            
            if !isCompact {
                // ËøõÂ∫¶Êù°
                VStack(alignment: .leading, spacing: 4) {
                    HStack {
                        Text("ËøõÂ∫¶: \(task.progressPercentage)%")
                            .font(.caption)
                            .foregroundColor(.secondary)
                        Spacer()
                        if task.timeSpent > 0 {
                            Text("‚è±Ô∏è \(task.formattedTimeSpent)")
                                .font(.caption)
                                .foregroundColor(.secondary)
                        }
                    }
                    
                    ProgressView(value: task.workProgress, total: 100)
                        .progressViewStyle(LinearProgressViewStyle(tint: task.isCompleted ? .green : .blue))
                        .scaleEffect(x: 1, y: 0.8)
                }
                
                if task.hasProgressUpdate {
                    Text("ÊúÄÂêéÊõ¥Êñ∞: \(task.formattedLastUpdate)")
                        .font(.caption)
                        .foregroundColor(.secondary)
                }
            } else {
                // Á¥ßÂáëÊ®°ÂºèÂè™ÊòæÁ§∫ËøõÂ∫¶ÁôæÂàÜÊØî
                HStack {
                    Text("\(task.progressPercentage)%")
                        .font(.caption)
                        .foregroundColor(.secondary)
                    if task.timeSpent > 0 {
                        Text("‚Ä¢ \(task.formattedTimeSpent)")
                            .font(.caption)
                            .foregroundColor(.secondary)
                    }
                    Spacer()
                }
            }
        }
        .padding(12)
        .background(task.isCompleted ? Color.green.opacity(0.05) : Color(.systemGray6))
        .cornerRadius(8)
        .overlay(
            RoundedRectangle(cornerRadius: 8)
                .stroke(task.isCompleted ? Color.green.opacity(0.3) : Color.clear, lineWidth: 1)
        )
    }
}

